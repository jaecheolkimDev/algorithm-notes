/**
 * NaN 속성은 Not-A-Number(숫자가 아님)를 나타냅니다.
 * Infinity 전역 속성은 무한대를 나타내는 숫자값입니다.
 */


console.log("rest : ", 0/0); // NaN
console.log("rest : ", 0%0); // NaN
console.log("rest : ", 0/2); // 0
console.log("rest : ", 0%2); // 0
console.log("rest : ", 1/0); // Infinity
console.log("rest : ", 1%0); // NaN
console.log("rest : ", 1/2); // 0.5
console.log("rest : ", 1%2); // 1
console.log("rest : ", 2%2); // 1
console.log("rest : ", 3%2); // 1
console.log("rest : ", 4%2); // 1
console.log("rest : ", 5%2); // 1
